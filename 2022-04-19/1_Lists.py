# Lists - general
my_list = [1, 2, 3, 4, 5]
x_pop = my_list.pop()
print(my_list)
print(x_pop)

# Lists - Sorting
my_list = [4, 5, 3, 7, 2, 1]
my_list.sort()
print(my_list)
my_list.reverse()
print(my_list)

# Lists - Slicing
my_list = [1, 2, 3, 4, 5]
print(my_list[2:])

# Lists - Slicing
my_list = [1, 2, 3, 4, 5]
another_list = ['a', 'b']
print(my_list + another_list)
